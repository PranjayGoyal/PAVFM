#include<iostream>
using namespace std;

int main(){
    int a =12;
    cout<<"size of integer is "<<sizeof(a)<<endl;

    float b;
    cout<<"size of float is "<<sizeof(b)<<endl;

    char c;
    cout<<"size of char is "<<sizeof(c)<<endl;
    
    bool d;
    cout<<"size of bool is "<<sizeof(d)<<endl;

    short int si;
    cout<<"size of short int is "<<sizeof(si)<<endl;

    long int q;
    cout<<"size of long int is "<<sizeof(q)<<endl;

}